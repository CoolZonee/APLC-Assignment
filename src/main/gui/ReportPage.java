    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yy");
            SimpleDateFormat fileFormat = new SimpleDateFormat("dd/MM/yy");
            String date = sdf.format(txtDate.getDate());
            String fileDate = fileFormat.format(txtDate.getDate());
            List<Order> orders = this.frame.admin.loadOrder();
            List<Order> orderMatched = new ArrayList<>();
            for (Order order: orders) {
                if(order.getDate().equals(fileDate)) {
                    orderMatched.add(order);
                }
            }
            this.frame.admin.generateReport(date, orderMatched);
            JOptionPane.showMessageDialog(frame, "Generated new report!", "Successful!",JOptionPane.INFORMATION_MESSAGE);
            this.refreshList();
        }
        catch (NullPointerException ex){
            JOptionPane.showMessageDialog(frame, "Please select a date!", "Error!",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnGenerateActionPerformed

