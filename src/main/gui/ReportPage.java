    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
        String fileName = ReportList.getSelectedValue();
        File file = new File(reportPath + fileName);
        if (file.exists()) {
            try {
                Desktop.getDesktop().open(file);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(frame, "Please select a file!", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(frame, "Please select a file!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
       
    }//GEN-LAST:event_btnOpenActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String fileName = ReportList.getSelectedValue();
        // if yes
        File file = new File(reportPath + fileName);
        if (file.exists()) {
            int n = JOptionPane.showConfirmDialog(frame, "Would you like to delete file: " + fileName + "?", "Delete", JOptionPane.YES_NO_OPTION);
            if(n == JOptionPane.YES_OPTION)
            {
                file.delete();
            }
            this.refreshList();
        } else {
            JOptionPane.showMessageDialog(frame, "Please select a file!", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

